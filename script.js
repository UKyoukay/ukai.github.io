// 現在表示されている問題のインデックス
let currentQuestionIndex = 0;
// 正解数
let correctAnswers = 0;
// 各問題の解答状況を記録 (0:未解答, 1:正解, 2:不正解)
let answerStatus = new Array(quizData.length).fill(0);

// 問題と選択肢を表示する関数
function displayQuestion(index) {
  const questionContainer = document.getElementById("quiz-container");

  // quiz-container の子要素を全て削除して初期化
  questionContainer.innerHTML = '';

  // 問題と解答状況表示領域を作成
  const quizSummary = document.createElement("div");
  quizSummary.id = "quiz-summary";
  quizSummary.innerHTML = `
    <p>解答状況: <span id="answered-count">${correctAnswers}</span> / <span id="total-count">${quizData.length}</span> 問正解</p>
    <p>正答率: <span id="accuracy-rate">${((correctAnswers / quizData.length) * 100).toFixed(2)}</span> %</p>
  `;
  questionContainer.appendChild(quizSummary);

  // 問題部分を作成
  const questionDiv = document.createElement("div");
  questionDiv.className = "question";

  const questionText = document.createElement("p");
  questionText.className = "question-text";
  questionText.textContent = quizData[index].question;
  questionDiv.appendChild(questionText);

  const optionsList = document.createElement("ul");
  optionsList.className = "options";

  quizData[index].options.forEach((option, i) => {
    const listItem = document.createElement("li");
    listItem.dataset.optionIndex = i; // data属性で選択肢のインデックスを保持

    const radioInput = document.createElement("input");
    radioInput.type = "radio";
    radioInput.name = "answer";
    radioInput.id = `option${i}`;
    radioInput.value = option;
    if (answerStatus[index] === 1) { // 正解していた場合
      radioInput.checked = (option === quizData[index].answer);
      radioInput.disabled = true; // 正解済みなので無効化
    } else if (answerStatus[index] === 2) { // 不正解だった場合
      radioInput.checked = (option === quizData[index].selectedAnswer); // ユーザーが選択した回答をチェック
      radioInput.disabled = true; // 不正解なので無効化
    }

    const label = document.createElement("label");
    label.htmlFor = `option${i}`;
    label.textContent = option;

    listItem.appendChild(radioInput);
    listItem.appendChild(label);
    optionsList.appendChild(listItem);
  });
  questionDiv.appendChild(optionsList);

  // 回答チェックボタン
  const checkButton = document.createElement("button");
  checkButton.className = "check-answer";
  checkButton.textContent = "解答を確認する";
  checkButton.disabled = answerStatus[index] !== 0; // 解答済みでなければ無効化

  // 解答済みの場合、フィードバックと解説を表示
  if (answerStatus[index] !== 0) {
    const resultDiv = document.createElement("div");
    resultDiv.className = "result";
    if (answerStatus[index] === 1) {
      resultDiv.classList.add("correct");
      resultDiv.textContent = "正解です！";
    } else {
      resultDiv.classList.add("incorrect");
      resultDiv.textContent = "不正解です。";
    }
    questionDiv.appendChild(resultDiv);

    const explanationDiv = document.createElement("div");
    explanationDiv.className = "explanation";
    explanationDiv.textContent = `解説: ${quizData[index].explanation}`;
    questionDiv.appendChild(explanationDiv);
  }

  questionDiv.appendChild(checkButton); // 回答チェックボタンを追加

  questionContainer.appendChild(questionDiv);

  // ナビゲーションボタンを作成
  const navButtonsDiv = document.createElement("div");
  navButtonsDiv.id = "nav-buttons";

  // 戻るボタン
  const backButton = document.createElement("button");
  backButton.id = "back-btn";
  backButton.textContent = "戻る";
  backButton.disabled = index === 0; // 最初の問題なら無効化
  navButtonsDiv.appendChild(backButton);

  // 次へボタン
  const nextButton = document.createElement("button");
  nextButton.id = "next-btn";
  nextButton.textContent = index === quizData.length - 1 ? "クイズ終了" : "次の問題へ";
  navButtonsDiv.appendChild(nextButton);

  questionContainer.appendChild(navButtonsDiv);

  // イベントリスナーの設定
  setupEventListeners();
}

// イベントリスナーを設定する関数
function setupEventListeners() {
  // ラジオボタンのクリックイベント
  const radioButtons = document.querySelectorAll('input[name="answer"]');
  radioButtons.forEach(radio => {
    radio.addEventListener('change', () => {
      const currentCheckButton = document.querySelector('.check-answer');
      if (currentCheckButton) {
        currentCheckButton.disabled = false; // ラジオボタンが選択されたらボタンを有効化
      }
    });
  });

  // 解答チェックボタンのイベント
  const checkButton = document.querySelector('.check-answer');
  if (checkButton) {
    checkButton.addEventListener('click', () => {
      const selectedOption = document.querySelector('input[name="answer"]:checked');
      if (!selectedOption) {
        alert("解答を選択してください。");
        return;
      }

      const userAnswer = selectedOption.value;
      const correctAnswer = quizData[currentQuestionIndex].answer;

      const resultDiv = document.createElement("div");
      resultDiv.className = "result";

      const explanationDiv = document.createElement("div");
      explanationDiv.className = "explanation";
      explanationDiv.textContent = `解説: ${quizData[currentQuestionIndex].explanation}`;

      if (userAnswer === correctAnswer) {
        if (answerStatus[currentQuestionIndex] === 0) { // 未解答の場合のみ正解数をカウント
          correctAnswers++;
        }
        answerStatus[currentQuestionIndex] = 1; // 正解
        resultDiv.classList.add("correct");
        resultDiv.textContent = "正解です！";
      } else {
        answerStatus[currentQuestionIndex] = 2; // 不正解
        quizData[currentQuestionIndex].selectedAnswer = userAnswer; // ユーザーの回答を保存
        resultDiv.classList.add("incorrect");
        resultDiv.textContent = "不正解です。";
      }
      // 問題部分に結果と解説を追加
      document.querySelector('.question').appendChild(resultDiv);
      document.querySelector('.question').appendChild(explanationDiv);
      checkButton.disabled = true; // 一度確認したら無効化

      // 解答状況を更新
      updateQuizSummary();
    });
  }

  // 戻るボタンのイベント
  const backButton = document.getElementById("back-btn");
  if (backButton) {
    backButton.addEventListener("click", () => {
      if (currentQuestionIndex > 0) {
        currentQuestionIndex--;
        displayQuestion(currentQuestionIndex);
      }
    });
  }

  // 次へボタンのイベント
  const nextButton = document.getElementById("next-btn");
  if (nextButton) {
    nextButton.addEventListener("click", () => {
      if (currentQuestionIndex < quizData.length - 1) {
        currentQuestionIndex++;
        displayQuestion(currentQuestionIndex);
      } else {
        displayResults();
      }
    });
  }
}

// 解答状況を表示する関数
function updateQuizSummary() {
  document.getElementById("answered-count").textContent = correctAnswers;
  document.getElementById("total-count").textContent = quizData.length;
  const accuracy = quizData.length > 0 ? (correctAnswers / quizData.length) * 100 : 0;
  document.getElementById("accuracy-rate").textContent = accuracy.toFixed(2);
}

// クイズ終了時の結果表示
function displayResults() {
  const questionContainer = document.getElementById("quiz-container");
  questionContainer.innerHTML = ''; // クイズコンテナをクリア

  const resultsDiv = document.createElement("div");
  resultsDiv.id = "quiz-results";
  resultsDiv.innerHTML = `
    <h2>テスト結果</h2>
    <p>総問題数: ${quizData.length} 問</p>
    <p>正解数: ${correctAnswers} 問</p>
    <p>正答率: ${((correctAnswers / quizData.length) * 100).toFixed(2)} %</p>
    <button id="restart-btn">もう一度挑戦する</button>
  `;
  questionContainer.appendChild(resultsDiv);

  document.getElementById("restart-btn").addEventListener("click", () => {
    currentQuestionIndex = 0;
    correctAnswers = 0;
    answerStatus.fill(0); // 解答状況をリセット
    displayQuestion(currentQuestionIndex);
  });
}

